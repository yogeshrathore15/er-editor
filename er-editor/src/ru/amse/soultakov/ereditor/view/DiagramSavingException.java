package ru.amse.soultakov.ereditor.view;

@SuppressWarnings("serial")
public class DiagramSavingException extends Exception {

    public DiagramSavingException() {
        super();
    }

    public DiagramSavingException(String message, Throwable cause) {
        super(message, cause);
    }

    public DiagramSavingException(String message) {
        super(message);
    }

    public DiagramSavingException(Throwable cause) {
        super(cause);
    }

}
