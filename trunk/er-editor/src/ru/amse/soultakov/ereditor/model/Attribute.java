package ru.amse.soultakov.ereditor.model;

/**
 * @author sma
 *
 */
public class Attribute
{
	/**
	 * 
	 */
	private String name;

	/**
	 * @param name
	 */
	public Attribute(String name)
	{
		super();
		this.name = name;
	}

	/**
	 * @return
	 */
	public String getName()
	{
		return name;
	}

	/**
	 * @param name
	 */
	public void setName(String name)
	{
		this.name = name;
	}
	
}
